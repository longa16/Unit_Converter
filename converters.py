def pound_to_kg(lb):
    return lb / 2.2046

def kg_to_pound(kg):
    return kg * 2.2046

def inch_to_meter(inch):
    return inch / 39.37

def meter_to_inch(m):
    return m * 39.37

def fahrenheit_to_celsius(f):
    return (f - 32) * 5 / 9

def celsius_to_fahrenheit(c):
    return (c * 9 / 5) + 32

def gallon_to_liter(g):
    return g * 3.78541

def liter_to_gallon(l):
    return l / 3.78541
